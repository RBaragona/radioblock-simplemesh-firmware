#include <stdarg.h>
#include <stdio.h>
#include <string.h>
#include "nwk.h"

/*****************************************************************************
*****************************************************************************/
void log_open(void)
{
  UBRR1H = 0;
  UBRR1L = 25;
  UCSR1A = (1 << U2X1);
  UCSR1B = (1 << TXEN1);
  UCSR1C = (3 << UCSZ10);
}

/*****************************************************************************
*****************************************************************************/
void write(uint8_t message)
{
  UDR1 = message;
  while (!(UCSR1A & (1 << UDRE1)));
}
